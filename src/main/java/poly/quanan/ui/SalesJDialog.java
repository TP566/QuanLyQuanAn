    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.loadCards(); // tải và hiển thị các thẻ lên cửa sổ bán hàng    
    }

    @Override
    public void showHoaDonJDialog(int cardId) {
        HoaDonDAO dao = new HoaDonDAOimpl();
        HoaDon bill = dao.findServicingByCardId(cardId); // Tải bill đang phục vụ của thẻ
        HoaDonJDialog dialog = new HoaDonJDialog((Frame) this.getOwner(), true);
        dialog.setHoaDon(bill); // ✅ TRUYỀN bill vào dialog trước khi hiển thị
        dialog.setVisible(true);
    }

    private void loadCards() {// tải và hiển thị các thẻ lên cửa sổ bán hàng
        CardDAO dao = new CardDAOimpl();
        List<Card> cards = dao.findAll();
        pnlCard.removeAll();
        cards.forEach(card -> pnlCard.add(this.createButton(card)));
    }

    private JButton createButton(Card card) { // <-- sửa lại cho đúng class tên Card

        JButton btnCard = new JButton();

        // Sử dụng đúng getter được Lombok sinh ra: getCardID()
        btnCard.setText(String.format("Card #%d", card.getId()));
        btnCard.setPreferredSize(new Dimension(0, 80));

        // Trạng thái 0 là đang hoạt động → enable
        btnCard.setEnabled(card.getStatus() == 0);

        // Đặt màu nền tùy theo trạng thái
        btnCard.setBackground(card.getId() == 0 ? Color.PINK : Color.PINK);

        // Truyền cardID làm lệnh hành động
        btnCard.setActionCommand(String.valueOf(card.getId()));

        btnCard.addActionListener((ActionEvent e) -> {
            int cardId = Integer.parseInt(e.getActionCommand());
            SalesJDialog.this.showHoaDonJDialog(cardId);
        });

        return btnCard;
    }

